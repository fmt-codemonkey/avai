// Constants
// =============================================================================

const AUDIT_CONFIG = {
  TOTAL_DURATION: 60, // seconds
  UPDATE_INTERVAL: 1000, // milliseconds
  PROGRESS_UPDATE_DELAY: 2000, // milliseconds
} as const;

