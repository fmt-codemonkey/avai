// =============================================================================
// Type Definitions
// =============================================================================

interface SecurityAuditModalProps {
  isOpen: boolean;
  onClose: () => void;
  onStartAudit: (prompt: string, repositoryLink?: string) => void;
}

type AuditPhaseStatus = 'pending' | 'active' | 'completed';
type AuditStage = 'setup' | 'progress' | 'results';
type VulnerabilitySeverity = 'Critical' | 'High' | 'Medium' | 'Low';

interface AuditPhase {
  readonly id: string;
  readonly name: string;
  readonly description: string;
  readonly icon: React.ComponentType<{ className?: string }>;
  readonly duration: number;
  status: AuditPhaseStatus;
}

interface VulnerabilityDetails {
  readonly id: string;
  readonly type: string;
  readonly severity: VulnerabilitySeverity;
  readonly description: string;
  readonly file: string;
  readonly recommendation: string;
}

interface AuditSummary {
  readonly totalIssues: number;
  readonly critical: number;
  readonly high: number;
  readonly medium: number;
  readonly low: number;
  readonly score: number;
  readonly grade: string;
}

interface AuditResults {
  readonly summary: AuditSummary;
  readonly vulnerabilities: readonly VulnerabilityDetails[];
}

