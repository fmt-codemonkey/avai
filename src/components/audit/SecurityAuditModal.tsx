/**
 * Generates a comprehensive PDF report content from audit results
 * @param results - The audit results containing summary and vulnerabilities
 * @param prompt - The original audit instructions provided by user
 * @returns Formatted string content for PDF export
 */
const generateReportContent = (results: AuditResults, prompt: string): string => {
  const currentDate = new Date().toLocaleDateString();
  const vulnerabilityList = results.vulnerabilities
    .map((vuln, index) => 
      `${index + 1}. ${vuln.type} (${vuln.severity})\n` +
      `   Location: ${vuln.file}\n` +
      `   Description: ${vuln.description}\n` +
      `   Recommendation: ${vuln.recommendation}\n`
    )
    .join('\n');

  return [
    'AVAI SECURITY AUDIT REPORT',
    '============================',
    `Generated: ${currentDate}`,
    '',
    'SUMMARY',
    '=======',
    `Total Issues Found: ${results.summary.totalIssues}`,
    `Security Score: ${results.summary.score}/10 (Grade ${results.summary.grade})`,
    '',
    'Severity Breakdown:',
    `• Critical: ${results.summary.critical}`,
    `• High: ${results.summary.high}`,
    `• Medium: ${results.summary.medium}`,
    `• Low: ${results.summary.low}`,
    '',
    'VULNERABILITIES FOUND',
    '=====================',
    vulnerabilityList,
    'AUDIT INSTRUCTIONS',
    '==================',
    prompt,
    '',
    'Generated by AVAI Security Audit System'
  ].join('\n');
};

/**
 * Resets all audit-related state to initial values
 * @param stateSetters - Object containing all state setter functions
 */
const resetAuditState = (stateSetters: {
  setShowResults: (value: boolean) => void;
  setIsAuditing: (value: boolean) => void;
  setPrompt: (value: string) => void;
  setRepositoryLink: (value: string) => void;
  setProgress: (value: number) => void;
  setTimeElapsed: (value: number) => void;
  setCurrentPhase: (value: number) => void;
}): void => {
  const {
    setShowResults,
    setIsAuditing,
    setPrompt,
    setRepositoryLink,
    setProgress,
    setTimeElapsed,
    setCurrentPhase
  } = stateSetters;

  setShowResults(false);
  setIsAuditing(false);
  setPrompt("");
  setRepositoryLink("");
  setProgress(0);
  setTimeElapsed(0);
  setCurrentPhase(0);
};

